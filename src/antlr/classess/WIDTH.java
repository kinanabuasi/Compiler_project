package antlr.classess;

public class WIDTH {
    
    int NUMBER;

    public int getNUMBER() {
        return NUMBER;
    }

    public void setNUMBER(int NUMBER) {
        this.NUMBER = NUMBER;
    }

    @Override
    public String toString() {
        return "WIDTH{" +
                 Integer.toString(NUMBER) +
                "}";
    }
}
